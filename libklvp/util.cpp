const uint8_t lcss::UniversalMetadataSetKey[] = { 0x06,0x0E,0x2B,0x34,
0x02,0x01,0x01,0x01,
0x0E,0x01,0x01,0x02,
0x01,0x01,0x00,0x00 };

bool lcss::isLDSGroup(unsigned char key[], int len)
{
	if (key == nullptr || len == 0)
		return false;

	for (int i = 0; i < len; i++)
	{
		if (i > 15)
			return false;
		if (key[i] != lcss::LocalSetKey[i++])
			return false;
	}
	return true;
}